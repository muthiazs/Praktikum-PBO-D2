class Bird extends Animal {
    public void takeOff() {
        System.out.println("Burung sedang mengepakkan sayap untuk lepas landas");
    }

    public void land() {
        System.out.println("Burung sedang mendarat");
    }

    public void fly() {
        System.out.println("Burung sedang terbang");
    }

    public void buildNest() {
        System.out.println("Burung sedang membuat sarang");
    }

    public void layEggs() {
        System.out.println("Burung sedang bertelur");
    }
}